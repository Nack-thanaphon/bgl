								<span class="pic w_frame alignright"><img src="images/aboutusblue.jpg" alt=""></span>
							  <p >&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; ในปีพุทธศักราช 2537 มหาวิทยาลัยบูรพา จัดตั้งภาค วิชาวัสดุศาสตร์ – เทคโนโลยีอัญมณี ขึ้นโดยสังกัดกับคณะวิทยาศาสตร์ มหาวิทยาลัยบูรพา และเริ่มรับนิสิตรุ่นแรกตั้งแต่ปีการศึกษา 2538 ต่อมามหาวิทยาลัยมีนโยบายขยายโอกาสทางการศึกษาไปยังภูมิภาคของรัฐบาลโดยการจัดตั้งวิทยาเขตแห่งใหม่ขึ้น ณ จังหวัดจันทบุรี โดยให้ชื่อว่า <strong>มหาวิทยาลัยบูรพา</strong> วิทยาเขตสารสนเทศจันทบุรี ในปีพุทธศักราช 2539 ในขณะเดียวกันได้จัดตั้งวิทยาลัยอัญมณีขึ้น ณ วิทยาเขตสารสนเทศจันทบุรีอีกด้วย ต่อมาภายหลังมหาวิทยาลัยได้เปลี่ยนชื่อ จากมหาวิทยาลัยบูรพา วิทยาเขตสารสนเทศจันทบุรี เป็น มหาวิทยาลัยบูรพา วิทยาเขตจันทบุรี เมื่อวันที่ 1 มิถุนายน 2552 จนถึงปัจจุบัน และให้เปลี่ยนชื่อจากวิทยาลัยอัญมณีเป็นคณะอัญมณี มหาวิทยาลัยบูรพา <strong>วิทยาเขตจันทบุรี</strong> โดยคณะอัญมณีมีภารกิจหลักในการผลิตบัณฑิต หลักสูตร วท.บ (อัญมณีและเครื่องประดับ) หลักสูตร บธ.บ (ธุรกิจอัญมณีและเครื่องประดับ) และหลักสูตร ศป.บ. (การออกแบบเครื่องประดับ) </p>
				        <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;นอกเหนือจากการผลิตบัณฑิต<strong> คณะอัญมณี</strong>ยังเล็งเห็นถึงความสำคัญในการให้บริการวิชาการแก่ชุมชน ซึ่งนับว่าเป็นหนึ่งในสี่ของภารกิจหลักของมหาวิทยาลัย</p>
								<span class="pic w_frame alignleft"><img src="images/aboutusred.jpg" alt=""></span>
