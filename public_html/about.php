                        <p>ในปี พ.ศ. 2556 สมาคมผู้ค้าอัญมณีและเครื่องประดับ จังหวัดจันทบุรี ได้ย้ายที่ทำการมาที่อาคารศูนย์ส่งเสริมอัญมณีและเครื่องประดับจังหวัดจันทบุรี ถนนมหาราช ต.ตลาด อ.เมือง จ.จันทบุรี ทำให้ห้องปฏิบัติการวิเคราะห์อัญมณี <strong>มหาวิทยาลัยบูรพา</strong> ต้องย้ายที่เปิดทำการด้วยเช่นกัน โดยเปิดให้บริการวันจันทร์ – วันพฤหัสบดี เวลา 8.30 น. – 16.30 น. ส่วนวันศุกร์และวันเสาร์ เปิดให้บริการที่ เวลา 9.00 น. – 17.00 น. จนถึงปัจจุบัน</p>
